const mongoose = require("mongoose");
const DashboardSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    species: {
        type: String,
        required: true
    },
    breed: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    weight: {
        type: Number,
        required: true
    },
}, {timestamps: true});
mongoose.model("Dashboard", DashboardSchema);